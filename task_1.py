import re

with open('data.txt', 'r', encoding='utf-8') as f:
    s = f.read().strip()  # Считываем весь текст и убираем лишние пробелы/переносы по краям

# Режем строку везде, где видим 2 или более буквы Q подряд
# parts — это список кусков текста, которые лежали между этими "заборами" из Q
parts = re.split(r'Q{2,}', s)

# Если кусков меньше 3, значит у нас меньше двух "заборов" QQ
# (между ними ничего не могло зажаться), поэтому выводим 0
if len(parts) < 3:
    print(0)
else:
    # parts[1:-1] — выкидываем первый и последний куски (они не ограничены QQ с двух сторон)
    # max(..., key=len) — выбираем из оставшихся самый длинный кусок
    # len(...) — считаем, сколько в этом куске букв
    print(len(max(parts[1:-1], key=len)))